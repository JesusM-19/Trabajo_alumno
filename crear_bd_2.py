import sqlite3

def crear_db():
    conn = sqlite3.connect('estudiante.db')
    c = conn.cursor()
    c.execute("""
    CREATE TABLE IF NOT EXISTS estudiante (
        Nombre TEXT,
        Edad INTEGER,
        Carrera TEXT,
        Matricula TEXT
    )
    """)
    conn.commit()
    return conn, c

def insertar_estudiante(conn, c, nombre, edad, carrera, matricula):
    c.execute("INSERT INTO estudiante (Nombre, Edad, Carrera, Matricula) VALUES (?, ?, ?, ?)",
              (nombre, edad, carrera, matricula))
    conn.commit()
    print(f"Estudiante {nombre} insertado correctamente.")

